package com.company;

public class Calculator {

    // constructor for Calculator
    public Calculator(){}

    //Method for adding two numbers is created below:
    public int add_numbers (int num1, int num2) {
        int sum = num1 + num2;
        return sum;
    }

    public int subtract_numbers (int num1, int num2) {
       int sub = num1 - num2;
       return sub;

    }
}
